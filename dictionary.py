# DICTIONARY = {"KEY1": "VALUE", "KEY2": "VALUE", "KEY3": "VALUE".........}
# Dictionary element can be accessed with their key only -> DICTIONARY["KEY3"]
myDictionary = {"C1": "Bangladesh", "C2": "India", "C3": "Nepal", "C4": "Bhutan", "C5": "Pakistan"}
print(myDictionary)
print(myDictionary["C3"])
print("--------------------------")

# Dictionary in Python can also contains multiple data type.
mix_dic = {"k1": 100, "k2": True, "k3": "Bike", "k4": [1, 2, 3, "Hello", "World"], "K5": (10, 20, 30)}
print(mix_dic)
print(mix_dic["k2"])
print("--------------------------")
